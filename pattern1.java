public class pattern1 {
    public static void main(String[] args) {
        for(int i=1;i<5;i++){                        //  1
            for(int j=1;j<=i;j++){                  //   22
                System.out.print(i);                //   333
            }                                       //   4444
            System.out.println();
        }
    }
    
}
